#include "libftprintf.h"

int		ft_tolower(int c)
{
	if (ft_isalpha(c) == 1)
	{
		if (c >= 65 && c <= 90)
		{
			c = c + 32;
			return (c);
		}
		else
			return (c);
	}
	return (c);
}
